function HTSV_color_switch() {
	call_user_func( "HTSV_data_color_" . HTSV_table_get_identity_by_province( HTSV_province_get_request() )->color );
}

function HTSV_check_is_front_page() {
	return is_front_page() && str_contains( "https://$_SERVER[HTTP_HOST]$_SERVER[REQUEST_URI]", home_url() );
}

function HTSV_identity_handler() {

	if ( HTSV_check_is_front_page() ) {

		HTSV_color_switch();
		HTSV_identity_logo();
		HTSV_identity_name();
	}
}

function HTSV_identity_logo() {
	echo "<style>
.logo img{
max-width: 100px;
max-height: 120px;
content: url('" . HTSV_table_get_identity_by_province( HTSV_province_get_request() )->logo . "');
}
</style>";
}

function HTSV_identity_name() {

	echo HTSV_style_wrapper("h2.widget-title::before{
content: '" . HTSV_table_get_identity_by_province( HTSV_province_get_request() )->name . " ' ;
}");
}

function HTSV_style_wrapper($data){
	return "<style>".$data."</style>";
}

add_action( 'wp_footer', 'HTSV_identity_handler' )


